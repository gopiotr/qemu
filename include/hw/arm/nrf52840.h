/*
 * Nordic Semiconductor nRF52840 Series SOC Common Defines
 */

#ifndef NRF52840_H
#define NRF52840_H

#define NRF52840_FLASH_BASE      0x00000000
#define NRF52840_FICR_BASE       0x10000000
#define NRF52840_FICR_SIZE       0x00000100
#define NRF52840_UICR_BASE       0x10001000
#define NRF52840_SRAM_BASE       0x20000000

#define NRF52840_IOMEM_BASE      0x40000000
#define NRF52840_IOMEM_SIZE      0x20000000

#define NRF52840_PERIPHERAL_SIZE 0x00001000
#define NRF52840_CLOCK_BASE      0x40000000
#define NRF52840_UART_BASE       0x40002000
#define NRF52840_TWI_BASE        0x40003000
#define NRF52840_TIMER_BASE      0x40008000
#define NRF52840_RNG_BASE        0x4000D000
#define NRF52840_NVMC_BASE       0x4001E000
#define NRF52840_GPIO_BASE       0x50000000

#define NRF52840_PRIVATE_BASE    0xF0000000
#define NRF52840_PRIVATE_SIZE    0x10000000

#define NRF52840_PAGE_SIZE       4*1024

/* Trigger */
#define NRF52840_TRIGGER_TASK 0x01

/* Events */
#define NRF52840_EVENT_CLEAR  0x00

#define NRF52840_CLOCK_LFCLKSTAT 0x418

#endif
